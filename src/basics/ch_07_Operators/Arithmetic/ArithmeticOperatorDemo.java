package basics.ch_07_Operators.Arithmetic;

public class ArithmeticOperatorDemo {
public static void main(String[] args) {
	// "+" for addition
	int a; //Declaration
	a=2; //Initialization
	int b=4; //Declaration and initialization
	int result;
	result=a+b;
	System.out.println(result);
	// "-" for subtraction
	int c=10;
	int d=5;
	result=c-d;
	System.out.println(result);
	
	// "*" multiplication
	int e=2;
	int f=4;
	result=e*f;
	System.out.println(result);
	
	// "/" for division
	// return answer/quotient
	int g=20;
	int h=4;
	result= g/h;
	System.out.println(result);
	 
	// "%" for modulus
	// returns the remainder
	int i=9;
	int j=2;
	result=i%j;
	System.out.println(result); 
}
}
