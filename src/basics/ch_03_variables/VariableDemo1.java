package basics.ch_03_variables;

public class VariableDemo1 {
	public static void main(String[] args) {
		// 1st  number =2
		// 2nd number = 4
		int a = 6;
		int b = 3;
		
		//SYNTAX
		//datatype variableName= value;
		
		// addition
		System.out.println(a+b); //6
		// Subtraction
		System.out.println(b-a); //2
		// Multiplication
		System.out.println(a * b); //8
		//division
		System.out.println(b/a); //2
		 
	}
}
