package examples.a3;

public class Manager {
	private String name;
	private String city;
	public int age;

	public void setName(String name) {
		this.name = name;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getName() {
		return this.name;
	}

	public String getCity() {
		return this.city;
	}
	public String getAge() {
		return this.age;
	}

	public String getAge() {
		return this.age;
	}

}

